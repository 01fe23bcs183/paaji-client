// Helper functions
export function getBlogPosts(options = {}) {
    let posts = [...blogPosts];

    // Filter by category
    if (options.category) {
        posts = posts.filter(p => p.category.toLowerCase() === options.category.toLowerCase());
    }

    // Filter by tag
    if (options.tag) {
        posts = posts.filter(p => p.tags.includes(options.tag.toLowerCase()));
    }

    // Filter featured
    if (options.featured) {
        posts = posts.filter(p => p.featured);
    }

    // Search
    if (options.search) {
        const searchLower = options.search.toLowerCase();
        posts = posts.filter(p =>
            p.title.toLowerCase().includes(searchLower) ||
            p.excerpt.toLowerCase().includes(searchLower) ||
            p.tags.some(t => t.includes(searchLower))
        );
    }

    // Sort
    if (options.sort === 'oldest') {
        posts.sort((a, b) => new Date(a.publishedAt) - new Date(b.publishedAt));
    } else {
        posts.sort((a, b) => new Date(b.publishedAt) - new Date(a.publishedAt));
    }

    // Pagination
    const page = options.page || 1;
    const limit = options.limit || 10;
    const start = (page - 1) * limit;
    const paginatedPosts = posts.slice(start, start + limit);

    return {
        posts: paginatedPosts,
        total: posts.length,
        page,
        totalPages: Math.ceil(posts.length / limit),
    };
}

export function getBlogPost(slug) {
    return blogPosts.find(p => p.slug === slug);
}

export function getRelatedPosts(currentSlug, limit = 3) {
    const current = getBlogPost(currentSlug);
    if (!current) return [];

    return blogPosts
        .filter(p => p.slug !== currentSlug)
        .filter(p =>
            p.category === current.category ||
            p.tags.some(t => current.tags.includes(t))
        )
        .slice(0, limit);
}
