// Helper functions
export function getProducts(options = {}) {
    let filteredProducts = [...products];

    // Filter by featured
    if (options.featured) {
        filteredProducts = filteredProducts.filter(p => p.featured);
    }

    // Filter by category
    if (options.category) {
        filteredProducts = filteredProducts.filter(p =>
            p.category.toLowerCase() === options.category.toLowerCase()
        );
    }

    // Filter by price range
    if (options.minPrice !== undefined) {
        filteredProducts = filteredProducts.filter(p => p.price >= options.minPrice);
    }
    if (options.maxPrice !== undefined) {
        filteredProducts = filteredProducts.filter(p => p.price <= options.maxPrice);
    }

    // Search by name
    if (options.search) {
        const searchLower = options.search.toLowerCase();
        filteredProducts = filteredProducts.filter(p =>
            p.name.toLowerCase().includes(searchLower) ||
            p.description.toLowerCase().includes(searchLower)
        );
    }

    // Sort
    if (options.sort === 'price-low') {
        filteredProducts.sort((a, b) => a.price - b.price);
    } else if (options.sort === 'price-high') {
        filteredProducts.sort((a, b) => b.price - a.price);
    } else if (options.sort === 'rating') {
        filteredProducts.sort((a, b) => b.rating - a.rating);
    } else if (options.sort === 'newest') {
        filteredProducts.sort((a, b) => (b.isNew ? 1 : 0) - (a.isNew ? 1 : 0));
    }

    // Limit
    if (options.limit) {
        filteredProducts = filteredProducts.slice(0, options.limit);
    }

    return filteredProducts;
}

export function getProduct(slug) {
    return products.find(p => p.slug === slug);
}

export function getFeaturedProducts(limit = 3) {
    return products.filter(p => p.featured).slice(0, limit);
}

export function getProductsByCategory(category, limit) {
    const filtered = products.filter(p =>
        p.category.toLowerCase() === category.toLowerCase()
    );
    return limit ? filtered.slice(0, limit) : filtered;
}

export default {
    products,
    getProducts,
    getProduct,
    getFeaturedProducts,
    getProductsByCategory,
};
